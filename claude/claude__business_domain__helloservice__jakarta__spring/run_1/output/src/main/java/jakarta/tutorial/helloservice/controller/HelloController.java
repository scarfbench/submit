package jakarta.tutorial.helloservice.controller;

import jakarta.tutorial.helloservice.service.HelloService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

/**
 * HelloController is a REST endpoint that exposes the hello service.
 */
@RestController
@RequestMapping("/hello")
public class HelloController {

    private final HelloService helloService;

    @Autowired
    public HelloController(HelloService helloService) {
        this.helloService = helloService;
    }

    @GetMapping
    public String sayHello(@RequestParam(name = "name", defaultValue = "World") String name) {
        return helloService.sayHello(name);
    }
}
